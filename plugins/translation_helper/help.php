<b>D2U Helper - Übersetzungshilfe Plugin</b>

<br /><br />
<p>Bietet für mehrsprachige Addons von D2U eine Übersetzungshilfe an. Dabei muss
	der Nutzer einem Datensatz in einem der D2U Addons öffnen und dann im Bereich
	der jeweiligen Sprache einstellen, ob die Übersetzung aktuell ist,
	aktualisiert werden muss oder gar nicht gewünscht ist. Aufgrund dieser Daten
	erstellt dieses Plugin eine Liste aller fehlenden oder zu aktualisierenden
	Übersetzungen.</p>