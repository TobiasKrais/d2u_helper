<b>D2U Helper Package</b>

<br /><br />
<p>Beinhaltet Klassen, die in mehreren Addons von www.design-to-use.de gebraucht
	werden.</p>
<p>Features:</p>
<ul>
	<li>Bietet Klassen die von anderen D2U Addons genutzt werden, z.B. die
	Modulverwaltung</li>
	<li>Verwaltung und Autoupdate von Modulen, die vom Autor angeboten werden.</li>
	<li>Verwaltung und Autoupdate von Templates, die vom Autor angeboten werden.</li>
	<li>Module und Templates sind in den Einstellungen anpassbar.</li>
</ul>